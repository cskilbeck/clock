constexpr nullptr_t null = nullptr;

//////////////////////////////////////////////////////////////////////

template <typename T, size_t N> constexpr size_t countof(T const (&)[N]) noexcept
{
    return N;
}

//////////////////////////////////////////////////////////////////////

template <typename T> T min(T const &a, T const &b)
{
    return (a < b) ? a : b;
}

//////////////////////////////////////////////////////////////////////

template <typename T> T max(T const &a, T const &b)
{
    return (a > b) ? a : b;
}
